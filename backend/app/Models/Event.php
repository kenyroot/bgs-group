    */

    /*
    |--------------------------------------------------------------------------
    | SCOPES
    |--------------------------------------------------------------------------
    */
    public function scopeByDateRange($query, $dates)
    {

        if (isset($dates->from)) {
            $query = $query->where('date', '>=', $dates->from->format('Y-m-d'));
        }

        if (isset($dates->to)) {
            $query = $query->where('date', '<', $dates->to->format('Y-m-d'));
        }

        return $query;
    }
